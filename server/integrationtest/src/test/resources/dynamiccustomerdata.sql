CREATE TABLE NEWDB_CAMPAIGN_CONF(
CAMPAIGN_ID VARCHAR(15) NOT NULL,
CAMPAIGN_TYPE VARCHAR(30),
STATUS VARCHAR(30),
START_DATE DATE,
END_DATE DATE,
OPERATION_DATE DATE NOT NULL
);

CREATE TABLE NEWDB_ASSET(
CUSTOMER_ID VARCHAR(15),
